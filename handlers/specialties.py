async def show_specialty_info(callback: types.CallbackQuery):
    spec_key = callback.data
    if spec_key in specialties_info:
        spec = specialties_info[spec_key]
        text = f"🔹 *{spec['name']}*\n\n"
        text += f"📝 *Описание:* {spec['description']}\n\n"
        text += f"⏱ *Срок обучения:* {spec['duration']}\n"
        text += f"📚 *Предметы:* {spec['subjects']}\n"
        text += f"💼 *Кем работать:* {spec['career']}\n"
        text += f"🎓 *Места:* {spec['places']}\n\n"
        text += "Хочешь узнать больше? Обратись в приемную комиссию!"
        keyboard = InlineKeyboardMarkup(
            inline_keyboard=[
                [InlineKeyboardButton(text="◀️ К списку специальностей", callback_data="back_to_specs")]
            ]
        )
        await callback.message.edit_text(text, reply_markup=keyboard, parse_mode="Markdown")
    else:
        await callback.answer("Информация не найдена")

def register_handlers(dp):
    for spec_key in specialties_info.keys():
        dp.callback_query.register(show_specialty_info, lambda c, key=spec_key: c.data == key)